import Head from "next/head";
import styles from "@/styles/VerificationUI.module.scss";
import { handleVerifyEmail } from "@/lib/auth_handle_email_verification";
import { auth } from "@/firebase";
import { useEffect, useState } from "react";
import { FaSpinner } from "react-icons/fa6";
import { useRouter } from "next/router";

const EmailVerificationUI = ({ actionCode}) => {
  const [ui, setUi] = useState(null);

  const router = useRouter()

  // send user to home page
  const handleClick = () => router.push('/')

  // verify user's email address when component loads initially
  useEffect(() => {
    handleVerifyEmail(auth, actionCode, setUi);
  }, []);

  // if ui is null, show a loading spinner
  if (!ui) return <FaSpinner style={{fontSize: `4rem`}} />;

  // show ui depending on ui message status
  if (ui && ui == "success") {
    return (
      <>
        <Head>
          <title>Email Verification </title>
          <meta name="description" content="Generated by create next app" />
          <meta name="viewport" content="width=device-width, initial-scale=1" />
        </Head>
        <main className={styles.veri_page}>
          <section>
            <p className={styles.confirmation_text}>
              Your email has been successfully verified
            </p>
            <button className={styles.continue_btn} onClick={handleClick}>
              continue to home page
            </button>
          </section>
        </main>
      </>
    );
  }

  if (ui && ui == "error") {
    return (
      <>
        <Head>
          <title>Email Verification </title>
          <meta name="description" content="Generated by create next app" />
          <meta name="viewport" content="width=device-width, initial-scale=1" />
        </Head>
        <main className={styles.veri_page}>
          <section>
            <p className={styles.confirmation_text}>
              Oops, there was an error, link has expired.
            </p>
            <p>Try verifying your email address again.</p>
            <button className={styles.continue_btn} onClick={handleClick}>
              continue to home page
            </button>
          </section>
        </main>
      </>
    );
  }
};
export default EmailVerificationUI;
